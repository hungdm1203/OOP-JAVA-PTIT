package J07036;

public class MonHoc {
    private String id,name;
    private int stc;

    public MonHoc(String id, String name, int stc) {
        this.id = id;
        this.name = name;
        this.stc = stc;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getStc() {
        return stc;
    }
    
}
