package J07060;

public class MonThi {
    private String id,name,hinhthuc;

    public MonThi(String id, String name, String hinhthuc) {
        this.id = id;
        this.name = name;
        this.hinhthuc = hinhthuc;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getHinhthuc() {
        return hinhthuc;
    }
    
    
}
