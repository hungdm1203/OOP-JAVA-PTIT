package J07074;

public class MonHoc {
    private String id,name,stc;

    public MonHoc(String id, String name, String stc) {
        this.id = id;
        this.name = name;
        this.stc = stc;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getStc() {
        return stc;
    }

    
    
}
